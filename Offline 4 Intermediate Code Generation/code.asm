.MODEL SMALL
.STACK 100H

.DATA
	CR EQU 0DH
	LF EQU 0AH
	VAR_TO_PRINT DW ?
	COUNT DW ?

	i_1 DW ?
	j_2 DW ?
	k_3 DW ?

.CODE
MAIN PROC
	;DATA INIT
	MOV AX, @DATA
	MOV DS, AX


	PUSH 3
	;i = 3
	POP CX
	MOV i_1, CX
	PUSH 8
	;j = 8
	POP CX
	MOV j_2, CX
	PUSH 6
	;k = 6
	POP CX
	MOV k_3, CX
	PUSH 3
	;Check i == 3
	MOV AX, i_1
	POP BX
	CMP AX, BX
	JE label_1
	PUSH 0
	JMP label_2

label_1:
	PUSH 1

label_2:

	;if (i==3)
	POP AX
	CMP AX, 1
	JNE label_3
	;println(j)
	MOV CX, j_2
	MOV VAR_TO_PRINT, CX
	CALL NEW_LINE
	CALL PRINT_VAR
	CALL NEW_LINE

label_3:
	PUSH 8
	;Check j < 8
	MOV AX, j_2
	POP BX
	CMP AX, BX
	JL label_4
	PUSH 0
	JMP label_5

label_4:
	PUSH 1

label_5:

	;if (j<8) else
	POP AX
	CMP AX, 1
	JNE label_6
	;println(i)
	MOV CX, i_1
	MOV VAR_TO_PRINT, CX
	CALL NEW_LINE
	CALL PRINT_VAR
	CALL NEW_LINE

	 JMP label_8

label_6:
	;println(k)
	MOV CX, k_3
	MOV VAR_TO_PRINT, CX
	CALL NEW_LINE
	CALL PRINT_VAR
	CALL NEW_LINE


label_8:
	PUSH 0

	;EXIT PROGRAM
	MOV AH, 4CH
	INT 21H

	MAIN ENDP

PRINT_VAR PROC

	PUSH AX
	PUSH BX
	PUSH CX
	PUSH DX

	MOV COUNT, 0

	MOV AX, VAR_TO_PRINT
	CMP AX, 0
	JL NEGATIVE_PRINT

START_DIVISION:
	MOV DX, 0
	MOV CX, 10
	DIV CX
	PUSH DX
	ADD COUNT, 1
	CMP AX, 0
	JE POP_STACK
	JMP START_DIVISION

NEGATIVE_PRINT:
	MOV CX, AX

	;PRINT MINUS SIGN
	MOV DL, '-'
	MOV AH, 2
	INT 21H

	MOV AX, CX
	NEG AX
	JMP START_DIVISION

POP_STACK:
	POP DX
	ADD DL, '0'
	MOV AH, 2
	INT 21H

	SUB COUNT, 1
	MOV CX, COUNT
	CMP CX, 0
	JNE POP_STACK

	POP DX
	POP CX
	POP BX
	POP AX

	RET

	PRINT_VAR ENDP


NEW_LINE PROC

	MOV AH, 2
	MOV DL, CR
	INT 21H
	MOV AH, 2
	MOV DL, LF
	INT 21H

	RET

	NEW_LINE ENDP


END MAIN
